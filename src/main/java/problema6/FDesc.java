    public void desc(int num, int limnum, String desco){
        if (num==0) {
            jTextArea1.append(desco+"\n");
            return;
        }
        
        for (int i = 1; i <= limnum; i++) {
            if (i<=num) {
                desc(num-i, i, desco+i+" ");
            }
        }
    }
    
